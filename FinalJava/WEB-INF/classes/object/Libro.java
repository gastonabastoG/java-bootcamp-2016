package object;

public class Libro {
	
	private String Titulo;
	private String Autor;
	private int id;

	public String getTitulo() {
	return Titulo;
	}

	public void setTitulo(String Titulo) {
	this.Titulo = Titulo;
	}

	public String getAutor() {
	return Autor;
	}

	public void setAutor(String Autor) {
	this.Autor = Autor;
	}

	public int getId() {
	return id;
	}
	
	

	public void setId(int id) {
	this.id = id;
	}
}